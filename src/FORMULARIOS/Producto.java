    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        int i = this.jTabla.getSelectedRow();
        if (i != -1) {

            int valor = JOptionPane.showConfirmDialog(this, "¿Seguro que desea ejecutar esta accion?", "Advertencia", JOptionPane.YES_NO_OPTION);
            if (valor == 0) {
                MProducto producto = new MProducto();
                producto.setCodigo(txtcodigo.getText());
                if (producto.eliminarProducto(producto)) {
                    JOptionPane.showMessageDialog(null, "Producto eliminado");
                    mostrar("");
                    inhabilitar();
                } else {
                    JOptionPane.showMessageDialog(null, "No se ha podido eliminar el Producto");
                }
            }
        } else {
            JOptionPane.showMessageDialog(null, "Selecione Fila a Eiliminar");
        }
    }//GEN-LAST:event_jButton1ActionPerformed

