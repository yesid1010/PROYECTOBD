    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed

        if (txtcodigo.getText().length() == 0) {
            JOptionPane.showMessageDialog(null, "Debes ingresar un codigo.");
            txtcodigo.requestFocus();
            return;
        }
        if (txtnombre.getText().length() == 0) {
            JOptionPane.showMessageDialog(null, "Debes ingresar un Nombre.");
            txtnombre.requestFocus();
            return;
        }
        if (txtcompra.getText().length() == 0) {
            JOptionPane.showMessageDialog(null, "Debes ingresar el Precio de compra.");
            txtcompra.requestFocus();
            return;
        }
        if (txtventa.getText().length() == 0) {
            JOptionPane.showMessageDialog(null, "Debes ingresar el Precio de venta.");
            txtventa.requestFocus();
            return;
        }

        if (txtstock.getText().length() == 0) {
            JOptionPane.showMessageDialog(null, "Debes ingresar una cantidad.");
            txtstock.requestFocus();
            return;
        }

        MProducto producto = new MProducto();

        producto.setCodigo(txtcodigo.getText());
        producto.setNombre(txtnombre.getText());
        producto.setExistencia(Integer.valueOf(txtstock.getText()));
        producto.setPrecio_compra(Integer.valueOf(txtcompra.getText()));
        producto.setPrecio_venta(Integer.valueOf(txtventa.getText()));
        String categoria = (String) txtcategoria.getSelectedItem();
        
        producto.setCategoria(Integer.valueOf(producto.GuardarCodigo(categoria)));
        
        JOptionPane.showMessageDialog(null, producto.getCategoria());
        if (producto.insertarProducto(producto)) {
            JOptionPane.showMessageDialog(null, "Producto registrado.");
            mostrar("");
            inhabilitar();
        } else {
            JOptionPane.showMessageDialog(null, "No se ingreso el Producto.");
            mostrar("");

        }
    }//GEN-LAST:event_btnGuardarActionPerformed

